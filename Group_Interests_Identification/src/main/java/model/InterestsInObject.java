package model;

import java.util.ArrayList;

public class InterestsInObject {
    ArrayList<Integer> advertisements;
    ArrayList<Integer> viewers;

    public void setAdvertisements(ArrayList<Integer> advertisements) {
        this.advertisements = advertisements;
    }

    public ArrayList<Integer> getAdvertisements() {
        return advertisements;
    }

    public void setViewers(ArrayList<Integer> viewers) {
        this.viewers = viewers;
    }

    public ArrayList<Integer> getViewers() {
        return viewers;
    }
}
